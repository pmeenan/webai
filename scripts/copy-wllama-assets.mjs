import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";

import { patchWllamaIndex } from "./patch-wllama-index.mjs";

const outputDirectory = path.resolve("public/runtime/wllama");
const manifestPath = path.resolve("src/lib/runtimes/wllama-assets.ts");
const mode = process.argv[2];
if (mode !== "--check" && mode !== "--update") {
  throw new Error("Usage: node scripts/copy-wllama-assets.mjs --check|--update");
}
const assets = [
  {
    manifestKey: "script",
    output: "index",
    source: "node_modules/@wllama/wllama/esm/index.js",
    suffix: "js",
    transform: (bytes) => Buffer.from(patchWllamaIndex(bytes.toString("utf8"))),
  },
  {
    manifestKey: "defaultWasm",
    output: "wllama",
    source: "node_modules/@wllama/wllama/src/wasm/wllama.wasm",
    suffix: "wasm",
  },
  {
    manifestKey: "compatWasm",
    output: "compat",
    source: "node_modules/@wllama/wllama-compat/wasm/wllama.wasm",
    suffix: "wasm",
  },
  {
    manifestKey: "compatWorker",
    output: "compat-worker",
    source: "node_modules/@wllama/wllama-compat/wasm/wllama.js",
    suffix: "js",
  },
];

fs.mkdirSync(outputDirectory, { recursive: true });
const deployedPaths = new Map();
for (const asset of assets) {
  const sourceBytes = fs.readFileSync(path.resolve(asset.source));
  const bytes = asset.transform === undefined ? sourceBytes : asset.transform(sourceBytes);
  const digest = crypto.createHash("sha256").update(bytes).digest("hex");
  const fileName = `${asset.output}.${digest.slice(0, 8)}.${asset.suffix}`;
  deployedPaths.set(asset.manifestKey, `/runtime/wllama/${fileName}`);
  const candidates = fs
    .readdirSync(outputDirectory)
    .filter(
      (existing) =>
        existing.startsWith(`${asset.output}.`) && existing.endsWith(`.${asset.suffix}`),
    );
  if (mode === "--check") {
    const deployed = path.join(outputDirectory, fileName);
    if (candidates.length !== 1 || candidates[0] !== fileName || !fs.existsSync(deployed)) {
      throw new Error(`The self-hosted ${asset.output} asset is stale; run the update script.`);
    }
    if (!fs.readFileSync(deployed).equals(bytes)) {
      throw new Error(`The self-hosted ${asset.output} asset does not match its pinned package.`);
    }
  } else {
    for (const existing of candidates) fs.rmSync(path.join(outputDirectory, existing));
    fs.writeFileSync(path.join(outputDirectory, fileName), bytes);
  }
  console.log(`${digest}  public/runtime/wllama/${fileName}`);
}

const manifest = `// Generated by scripts/copy-wllama-assets.mjs. Do not edit by hand.
export const wllamaRuntimeAssets = {
  script: "${deployedPaths.get("script")}",
  defaultWasm: "${deployedPaths.get("defaultWasm")}",
  compatWasm: "${deployedPaths.get("compatWasm")}",
  compatWorker: "${deployedPaths.get("compatWorker")}",
} as const;
`;
if (mode === "--check") {
  if (!fs.existsSync(manifestPath) || fs.readFileSync(manifestPath, "utf8") !== manifest) {
    throw new Error("The generated wllama runtime asset manifest is stale; run the update script.");
  }
} else {
  fs.writeFileSync(manifestPath, manifest);
}
